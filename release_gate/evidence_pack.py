def _bar_color(score: int) -> str:
    if score >= 75: return "bar-green"
    if score >= 50: return "bar-yellow"
    return "bar-red"


def _dim_card(name: str, info: Dict) -> str:
    s = info.get("score", 0)
    return (
        f'<div class="dim-card">'
        f'<div class="dim-name">{name.replace("_", " ").title()}</div>'
        f'<div class="dim-score">{s}</div>'
        f'<div class="bar-wrap"><div class="bar-fill {_bar_color(s)}" style="width:{s}%"></div></div>'
        f'</div>'
    )


def render_html_evidence(data: Dict[str, Any], output_path: str) -> str:
    score    = data.get("readiness_score", 0)
    decision = data.get("decision", "UNKNOWN")
    project  = data.get("project", "AI Agent")
    model    = data.get("model", "unknown")
    ts       = data.get("generated_at", datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC"))
    dims     = data.get("dimensions", {})
    crits    = data.get("critical_failures", [])
    evals    = data.get("eval_summary", {})
    traces   = data.get("trace_summary", {})
    impact   = data.get("impact_summary", {})
    checks   = data.get("check_results", {})
    conf     = data.get("confidence", "low")

    dim_cards = "\n".join(_dim_card(n, i) for n, i in dims.items())

    if crits:
        crit_rows = "".join(
            f'<div class="crit"><span class="crit-label">[{f["source"]}] {f["check"]}</span><br>{f["reason"]}</div>'
            for f in crits
        )
        critical_section = (
            '<h2>&#x26D4; Critical Failures</h2>'
            f'<div class="section">{crit_rows}</div>'
        )
    else:
        critical_section = '<h2>Critical Failures</h2><div class="section"><p class="pass">&#x2713; No critical failures detected.</p></div>'

    if evals and evals.get("total", 0) > 0:
        rows = "".join(
            f'<tr><td>{r.get("name","")}</td>'
            f'<td><code>{r.get("category","")}</code></td>'
            f'<td><code>{r.get("severity","")}</code></td>'
            f'<td class="{"pass" if r.get("passed") else "fail"}">'  
            f'{"&#x2713; PASS" if r.get("passed") else "&#x2717; FAIL"}</td>'
            f'<td>{r.get("failure_reason") or "&mdash;"}</td></tr>'
            for r in evals.get("results", [])
        )
        eval_section = (
            f'<h2>Eval Results &nbsp; <small>{evals["passed"]}/{evals["total"]} passed '
            f'({evals.get("pass_rate",0):.0f}%)</small></h2>'
            '<div class="section"><table>'
            '<tr><th>Test</th><th>Category</th><th>Severity</th><th>Result</th><th>Reason</th></tr>'
            f'{rows}</table></div>'
        )
    else:
        eval_section = '<h2>Eval Results</h2><div class="section"><p class="warn">No evals configured. Add an evals.yaml for LLM behavior testing.</p></div>'

    if traces:
        viols = traces.get("violations", [])
        warns = traces.get("warnings", [])
        css   = "fail" if viols else ("warn" if warns else "pass")
        viol_html = "".join(f"<li class='fail'>{v}</li>" for v in viols)
        warn_html = "".join(f"<li class='warn'>{w}</li>" for w in warns)
        trace_section = (
            f'<h2>Trace Analysis &nbsp; <small class="{css}">{traces.get("status","")}</small></h2>'
            f'<div class="section"><ul>{viol_html}{warn_html}</ul>'
            f'<p style="margin-top:8px;color:#8b949e">Traces: {traces.get("trace_count",0)}</p></div>'
        )
    else:
        trace_section = '<h2>Trace Analysis</h2><div class="section"><p class="warn">No trace file provided. Pass --traces to evaluate agent behavior.</p></div>'

    if impact:
        impact_section = (
            '<h2>Cost Risk</h2><div class="section"><table>'
            '<tr><th>Scenario</th><th>Daily</th><th>Monthly</th></tr>'
            f'<tr><td>Normal</td><td>${impact.get("normal_daily",0):,.2f}</td><td>${impact.get("normal_daily",0)*30:,.2f}</td></tr>'
            f'<tr style="color:#f85149"><td>Runaway</td><td>${impact.get("runaway_daily",0):,.2f}</td><td>${impact.get("runaway_daily",0)*30:,.2f}</td></tr>'
            f'<tr style="color:#d29922;font-weight:600"><td>Risk delta</td><td>${impact.get("risk_delta_daily",0):,.2f}</td><td>${impact.get("risk_delta_daily",0)*30:,.2f}</td></tr>'
            '</table></div>'
        )
    else:
        impact_section = ""

    if checks:
        rows = "".join(
            f'<tr><td>{name}</td>'
            f'<td class="{"pass" if r.get("status")=="PASS" else ("warn" if r.get("status")=="WARN" else "fail")}">'
            f'{r.get("status","UNKNOWN")}</td></tr>'
            for name, r in sorted(checks.items())
        )
        check_section = (
            '<h2>Governance Checks</h2><div class="section"><table>'
            '<tr><th>Check</th><th>Status</th></tr>'
            f'{rows}</table></div>'
        )
    else:
        check_section = ""

    html = _HTML_TEMPLATE.format(
        project=project, model=model, timestamp=ts,
        decision=decision, decision_lower=decision.lower(),
        score=score, confidence=conf,
        dim_cards=dim_cards,
        critical_section=critical_section,
        eval_section=eval_section,
        trace_section=trace_section,
        impact_section=impact_section,
        check_section=check_section,
    )

    Path(output_path).write_text(html, encoding="utf-8")
    return output_path


def generate_evidence_pack(data: Dict[str, Any], output_dir: str = "release-evidence") -> Dict[str, str]:
    """Write all three evidence artefacts and return their paths."""
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    json_path = str(out / "readiness_report.json")
    md_path   = str(out / "executive_summary.md")
    html_path = str(out / "release-gate-evidence.html")

    write_json_report(data, json_path)
    write_markdown_summary(data, md_path)
    render_html_evidence(data, html_path)

    return {"json": json_path, "markdown": md_path, "html": html_path}
