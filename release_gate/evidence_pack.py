"""
Evidence Pack generator for release-gate.

Produces an audit-ready set of artefacts:
  - readiness_report.json     full machine-readable evidence
  - executive_summary.md      one-page human summary
  - release-gate-evidence.html self-contained HTML dashboard

Usage:
    release-gate evidence-pack governance.yaml [options]
"""

from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, Optional


def write_json_report(data: Dict[str, Any], path: str) -> str:
    Path(path).write_text(json.dumps(data, indent=2), encoding="utf-8")
    return path


def write_markdown_summary(data: Dict[str, Any], path: str) -> str:
    score    = data.get("readiness_score", 0)
    decision = data.get("decision", "UNKNOWN")
    project  = data.get("project", "AI Agent")
    ts       = data.get("generated_at", "")
    dims     = data.get("dimensions", {})
    crits    = data.get("critical_failures", [])
    evals    = data.get("eval_summary", {})
    traces   = data.get("trace_summary", {})
    impact   = data.get("impact_summary", {})

    decision_emoji = {"PROMOTE": "✅", "HOLD": "⚠️", "BLOCK": "\U0001f6ab"}.get(decision, "❓")

    lines = [
        f"# AI Release Readiness — {project}",
        f"",
        f"**Generated:** {ts}  ",
        f"**Decision:** {decision_emoji} **{decision}**  ",
        f"**Readiness Score:** {score}/100  ",
        f"",
        f"---",
        f"",
        f"## Dimension Scores",
        f"",
        f"| Dimension | Score |",
        f"|-----------|-------|"  ,
    ]
    for dim, info in dims.items():
        bar = "█" * (info["score"] // 10) + "░" * (10 - info["score"] // 10)
        lines.append(f"| {dim.replace('_', ' ').title()} | {info['score']}/100  {bar} |")

    lines += ["", "---", ""]

    if crits:
        lines += ["## ⛔ Critical Failures", ""]
        for f in crits:
            lines.append(f"- **[{f['source'].upper()}]** `{f['check']}` — {f['reason']}")
        lines.append("")

    if evals:
        lines += [
            "## Eval Results", "",
            f"- Total: {evals.get('total', 0)}",
            f"- Passed: {evals.get('passed', 0)}",
            f"- Failed: {evals.get('failed', 0)}",
            f"- Critical failures: {evals.get('critical_failed', 0)}",
            f"- Pass rate: {evals.get('pass_rate', 0):.1f}%",
            "",
        ]

    if traces:
        lines += [
            "## Trace Analysis", "",
            f"- Violations: {len(traces.get('violations', []))}",
            f"- Unauthorized tool calls: {traces.get('unauthorized_tool_calls', [])}",
            "",
        ]

    if impact:
        lines += [
            "## Cost Risk", "",
            f"- Normal daily cost: ${impact.get('normal_daily', 0):,.2f}",
            f"- Runaway scenario:  ${impact.get('runaway_daily', 0):,.2f}",
            f"- Risk delta:        ${impact.get('risk_delta_daily', 0):,.2f}/day",
            "",
        ]

    recommendation = {
        "PROMOTE": "All required checks passed — meets the configured release policy.",
        "HOLD":    "Governance passed but score below threshold — manual approval required.",
        "BLOCK":   "One or more critical failures. Do NOT ship until issues are resolved.",
    }.get(decision, "")

    lines += [
        "---", "",
        "## Recommendation", "",
        recommendation, "",
        "_Generated by release-gate — github.com/VamsiSudhakaran1/release-gate_",
    ]

    Path(path).write_text("\n".join(lines), encoding="utf-8")
    return path


